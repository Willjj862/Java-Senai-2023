public class Exemplo03{

    public static final double BASE = 5.5;// A base é uma constante


    public static void main(String[] args) {
        
        double altura = 4.0;
        double are = (BASE*altura)/2;
        System.out.println("A área do triangulo é: " +are+ "cm²");
    }

}
